public class Contestsinfo {
    String title;
    String win_string;
    String seo_description;
    String src;
    String participate_src;
    String date_added;
    String date_expiry;
    String name;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getWin_string() {
        return win_string;
    }

    public void setWin_string(String win_string) {
        this.win_string = win_string;
    }

    public String getSeo_description() {
        return seo_description;
    }

    public void setSeo_description(String seo_description) {
        this.seo_description = seo_description;
    }

    public String getSrc() {
        return src;
    }

    public void setSrc(String src) {
        this.src = src;
    }

    public String getParticipate_src() {
        return participate_src;
    }

    public void setParticipate_src(String participate_src) {
        this.participate_src = participate_src;
    }

    public String getDate_added() {
        return date_added;
    }

    public void setDate_added(String date_added) {
        this.date_added = date_added;
    }

    public String getDate_expiry() {
        return date_expiry;
    }

    public void setDate_expiry(String date_expiry) {
        this.date_expiry = date_expiry;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
